    private void loadScreensToTable() {
        DefaultTableModel model = (DefaultTableModel) screenTable.getModel();
        model.setRowCount(0); // clear table

        try {
            FileReader fr = new FileReader("src\\theatre2\\data\\screens.txt");
            BufferedReader br = new BufferedReader(fr);
            String line;
            int no = 1;
            while ((line = br.readLine()) != null) {
                String[] p = line.split("\\|");
                if (p.length >= 5) {
                    // Order in file: name|capacity|status|cols|seatType
                    model.addRow(new Object[]{
                        no++,
                        p[0], // Screen Name
                        p[1], // Capacity
                        p[2], // Status
                        p[3], // Columns Per Row
                        p[4] // SeatTypes
                    });
                }
            }
            br.close();
            fr.close();
        } catch (java.io.FileNotFoundException e) {
            // First run: file may not exist. It's fine.
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading screens: " + e.getMessage());
        }
    }

