    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String name = screennameField.getText().trim();
        String capStr = capacityField.getText().trim();
        String status = (String) StatusComboBox.getSelectedItem();
        String colsStr = columnsPerRowField.getText().trim();
        String seat = (String) seatTypesComboBox.getSelectedItem();

        if (name.isEmpty() || capStr.isEmpty() || colsStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill Screen Name, Capacity, and Columns Per Row.");
            return;
        }

        // Very basic number checks
        try {
            Integer.parseInt(capStr);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Capacity must be a number.");
            return;
        }
        try {
            Integer.parseInt(colsStr);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Columns Per Row must be a number.");
            return;
        }

        String line = name + "|" + capStr + "|" + status + "|" + colsStr + "|" + seat;

        try {
            FileWriter fw = new FileWriter(filepath, true);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(line);
            bw.newLine();
            bw.close();
            fw.close();

            JOptionPane.showMessageDialog(this, "Screen saved.");
            loadScreensToTable();     // refresh table
            discardButtonActionPerformed(null); // clear form
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saving: " + e.getMessage());
        }
        loadScreensToTable();
    }//GEN-LAST:event_saveButtonActionPerformed

