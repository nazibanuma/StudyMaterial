    private void deleteScreenButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteScreenButtonActionPerformed
        // TODO add your handling code here:
        int row = screenTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) screenTable.getModel();

        // Values from table (must match how we saved to file)
        String name = (String) model.getValueAt(row, 1);
        String capStr = String.valueOf(model.getValueAt(row, 2));
        String status = (String) model.getValueAt(row, 3);
        String colsStr = String.valueOf(model.getValueAt(row, 4));
        String seat = (String) model.getValueAt(row, 5);

        String targetLine = name + "|" + capStr + "|" + status + "|" + colsStr + "|" + seat;

        // Read file, filter out the targetLine, write back
        try {
            List<String> lines = Files.readAllLines(Paths.get(filepath));
            List<String> newLines = new ArrayList<>();
            for (String line : lines) {
                if (!line.equals(targetLine)) { // basic match on full line
                    newLines.add(line);
                }
            }
            Files.write(Paths.get(filepath), newLines);

            // Remove from table and fix numbering
            model.removeRow(row);
            for (int i = 0; i < model.getRowCount(); i++) {
                model.setValueAt(i + 1, i, 0); // reset "No" column
            }

            JOptionPane.showMessageDialog(this, "Screen deleted.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error deleting: " + e.getMessage());
        }
    }//GEN-LAST:event_deleteScreenButtonActionPerformed


