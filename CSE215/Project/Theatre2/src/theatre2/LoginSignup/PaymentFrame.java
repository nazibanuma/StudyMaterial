    private void payButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payButtonActionPerformed
        // TODO add your handling code here:
        // Very light validation â€” adjust to your needs
        String card = cardNumberField.getText().trim();
        String exp  = expirationField.getText().trim(); // e.g., MM/YY
        String cvc  = CVCField.getText().trim();

        if (card.isEmpty() || exp.isEmpty() || cvc.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill card, expiration and CVC.");
            return;
        }
        if (!card.matches("\\d{12,19}")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Card number must be 12-19 digits.");
            return;
        }
        if (!cvc.matches("\\d{3,4}")) {
            javax.swing.JOptionPane.showMessageDialog(this, "CVC must be 3 or 4 digits.");
            return;
        }
        // Optional: very basic MM/YY check
        if (!exp.matches("(0[1-9]|1[0-2])\\/(\\d{2})")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Expiration must be MM/YY.");
            return;
        }

        // Simulate payment success â†’ notify parent via callback
        if (listener != null) {
            listener.onPaymentSuccess();
        }

        // Close this window
        dispose();
    }//GEN-LAST:event_payButtonActionPerformed


